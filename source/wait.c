/*****************************************************************************/
/*                               wait_us()                                   */
/*****************************************************************************/

void wait_us (uint16_t usecs)
{
  /* wait for microseconds using NOP cycle loop */
  _delay_us(usecs);
}

/*****************************************************************************/
/*                               wait_ms()                                   */
/*****************************************************************************/

void wait_ms (uint16_t msecs)
{
  /* wait for milliseconds using NOP cycle loop */
  _delay_ms(msecs);
}

